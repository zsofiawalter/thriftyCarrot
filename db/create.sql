CREATE TABLE Users (
    id INT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    email VARCHAR (255) NOT NULL,
    password VARCHAR(255) NOT NULL, 
    firstname VARCHAR(30) NOT NULL, 
    lastname VARCHAR(30) NOT NULL, 
    birthdate VARCHAR(25),
    joindate VARCHAR(25) 
);

-- Available products
CREATE TABLE Products (
    id INT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(50) NOT NULL,
    price DECIMAL(12,2) NOT NULL, 
    category VARCHAR(20) NOT NULL,
    store VARCHAR(15) NOT NULL,
    last_update timestamp without time zone NOT NULL DEFAULT (current_timestamp AT TIME ZONE 'UTC')
);

CREATE TABLE Preferences (
    uid INT NOT NULL REFERENCES Users(id),
    pid INT NOT NULL REFERENCES Products(id),
    like_dislike BOOLEAN,
    time_created timestamp without time zone NOT NULL DEFAULT (current_timestamp AT TIME ZONE 'UTC'),
    PRIMARY KEY(uid, pid)
);

-- CART IN PROGRESS
-- Stores current cart name
CREATE TABLE Carts (
    uid INT NOT NULL PRIMARY KEY REFERENCES Users(id),
    cart_name VARCHAR(30) NOT NULL,
    time_started timestamp without time zone NOT NULL DEFAULT (current_timestamp AT TIME ZONE 'UTC')
);

-- List of products in carts during initial step of building cart
CREATE TABLE CartLists (
    uid INT NOT NULL REFERENCES Users(id),
    product_name VARCHAR(50) NOT NULL
);

-- Stores products in current cart of users
CREATE TABLE CartContents (
    uid INT NOT NULL REFERENCES Users(id),
    pid INT NOT NULL REFERENCES Products(id),
    quantity INT CHECK (quantity > -1),
    PRIMARY KEY(uid, pid)
);

-- OLD CARTS
-- Stores users old carts name and 
CREATE TABLE OldCarts (
    cid INT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    uid INT NOT NULL REFERENCES Users(id),
    cart_name VARCHAR(30) NOT NULL,
    time_created timestamp without time zone NOT NULL DEFAULT (current_timestamp AT TIME ZONE 'UTC')
);

-- Stores old cart contents
CREATE TABLE OldCartContents (
    cid INT NOT NULL REFERENCES OldCarts(cid),
    pid INT NOT NULL,
    product_name VARCHAR(50) NOT NULL,
    price DECIMAL(12,2) NOT NULL, 
    category VARCHAR(15) NOT NULL,
    store VARCHAR (15) NOT NULL,
    quantity INT CHECK (quantity > 0),
    PRIMARY KEY(cid, pid)
);
